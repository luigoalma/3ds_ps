typedef struct {
	u8 mod[0x100];
	union {
		u32 small_exp; // = this member if is_full_exponent is 0, e.g. = 0x10001
		u8 exp[0x100]; // memcpy big endian number to this member if is_full_exponent is 1
	};
	s32 rsa_bit_size;
	u8 is_full_exponent; // 1 if big endian full exponent, 0 if small u32 exponent
	u8 padding[3];
} PS_RSA_Context;

_Static_assert(sizeof(PS_RSA_Context) == 0x208, "Invalid PS_RSA_Context size compiled");

Result PS_CustomCryptRsa(const PS_RSA_Context *ctx, const u8 *inbuf, u8 *outbuf)
{
	Result ret = 0;
	u32 *cmdbuf = getThreadCommandBuffer();
	u32 size;

	size = ctx->rsa_bit_size>>3;

	cmdbuf[0] = IPC_MakeHeader(0x401,0,6); // 0x4010006
	cmdbuf[1] = IPC_Desc_StaticBuffer(sizeof(*ctx), 0);
	cmdbuf[2] = (u32)ctx;
	cmdbuf[3] = IPC_Desc_Buffer(size, IPC_BUFFER_R);
	cmdbuf[4] = (u32)inbuf;
	cmdbuf[5] = IPC_Desc_Buffer(size, IPC_BUFFER_W);
	cmdbuf[6] = (u32)outbuf;

	if(R_FAILED(ret = svcSendSyncRequest(psGetSessionHandle()))) return ret;

	return (Result)cmdbuf[1];
}

