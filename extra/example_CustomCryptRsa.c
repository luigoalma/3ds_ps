	priv.rsa_bit_size = 2048;
	priv.is_full_exponent = 1;

	memcpy(pub.mod, n, 0x100);
	pub.small_exp = 0x10001;
	pub.rsa_bit_size = 2048;
	pub.is_full_exponent = 0;

